#ifndef CACHE_H
#define CACHE_H

#define WORD_SIZE 4                 // in bytes, i.e 32 bit words
#define BLOCK_SIZE (16 * WORD_SIZE)    // in bytes
#define DRAM_SIZE (1024 * BLOCK_SIZE) // in bytes
#define L1_SIZE (256 * BLOCK_SIZE)      // in bytes
#define L2_SIZE (512 * BLOCK_SIZE)    // in bytes

#define MODE_READ 1
#define MODE_WRITE 0

#define DRAM_READ_TIME 100
#define DRAM_WRITE_TIME 50
#define L2_READ_TIME 10
#define L2_WRITE_TIME 5
#define L1_READ_TIME 1
#define L1_WRITE_TIME 1

#define L1_LINES L1_SIZE / BLOCK_SIZE
#define L2_LINES L2_SIZE / BLOCK_SIZE
#define L2_SET_SIZE 2
#define L2_SETS L2_LINES / L2_SET_SIZE

#define OFFSET_BITS 6
#define INDEX_BITS_1W 8
#define INDEX_BITS_2W 7
#define INDEX_MASK_1W 0b00000000000000000011111111000000
#define TAG_MASK_1W   0b11111111111111111100000000000000
#define INDEX_MASK_2W 0b00000000000000000001111111000000
#define TAG_MASK_2W   0b11111111111111111110000000000000

#endif
